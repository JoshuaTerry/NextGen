                Zip = zip,
                Prefix = prefix,
                Street = street,
                Suffix = suffix,
                Suffix2 = suffix2,
                UrbanizationKey = urbKey,
                CityKey = cityKey,
                Id = GuidHelper.NextGuid()
            };
        }

